def get_comprehensive_analysis(card_name: str, position: str) -> str:
    """
    返回包含元素屬性、數字意涵、牌面象徵與正逆位詮釋的完整綜合分析。

    綜合分析邏輯：
    - 元素 → 揭示能量的本質與驅動力（火/水/風/土、星座/行星）
    - 數字 → 揭示此牌在週期中所處的位置與主題
    - 牌面象徵 → 視覺語言與深層隱喻的提煉
    - 正逆位詮釋 → 在當前脈絡下的實際能量表現

    :param card_name: 牌名，如 "愚者"、"聖杯 3"
    :param position: "正位" 或 "逆位"
    :return: 格式化的綜合分析文字；找不到時回傳空字串
    """
    card = TAROT_KNOWLEDGE.get(card_name)
    attrs = CARD_ATTRIBUTES.get(card_name, {})
    if not card:
        return ""

    pos_key = "reversed" if position == "逆位" else "upright"
    interpretation = card.get(pos_key, "")

    lines = [f"【{card_name} · {position}】"]

    element = attrs.get("element", "")
    if element:
        lines.append(f"元素屬性：{element}")

    number = attrs.get("number", "")
    if number:
        lines.append(f"數字意涵：{number}")

    image = attrs.get("image", "")
    if image:
        lines.append(f"牌面象徵：{image}")

    if interpretation:
        lines.append(f"\n詮釋：{interpretation}")

    return "\n".join(lines)
